const URL = "/one-api/mockup/masterdata/accounting/Beginingbalance";

export async function getPeriode(prm) {
    try {
        var resp = await axios.post(URL + '/getPeriode', prm);
        if (resp.status != 200) {
            return {
                status: "ERR",
                message: resp.statusText
            };
        }
        let data = resp.data;
        return data;
    } catch (e) {
        return {
            status: "ERR",
            message: e.message
        };
    }
}
