const URL = "/one-api/mockup/resultentrysoothers-cpone-v6/";

export async function savetypesds(prm) {
    try {
        var resp = await axios.post(URL + 'resultentry/savetypesds', prm);
        if (resp.status != 200) {
            return {
                status: "ERR",
                message: resp.statusText
            };
        }
        let data = resp.data;
        return data;
    } catch (e) {
        return {
            status: "ERR",
            message: e.message
        };
    }
}

