const URL = "/one-api/mockup/masterdata/accounting/Recrusivetemplate";

export async function searchDebet(prm) {
    try {
        var resp = await axios.post(URL + '/searchCoa', prm);
        if (resp.status != 200) {
            return {
                status: "ERR",
                message: resp.statusText
            };
        }
        let data = resp.data;
        return data;
    } catch (e) {
        return {
            status: "ERR",
            message: e.message
        };
    }
}
